export function numberWithCommas(x) {
  var parts = x.toString().split('.');
  parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ',');
  if (parts[1] && parseInt(parts[1]) !== 0) {
    return parts.join('.');
  } else {
    return parts[0];
  }
  // return x.toLocaleString();
}

